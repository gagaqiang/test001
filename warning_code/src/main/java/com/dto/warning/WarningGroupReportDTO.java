package com.dto.warning;

public class WarningGroupReportDTO {
    private int undoNum;
    private int isdoNum;
    private int totalNum;

    public int getUndoNum() {
        return undoNum;
    }

    public void setUndoNum(int undoNum) {
        this.undoNum = undoNum;
    }

    public int getIsdoNum() {
        return isdoNum;
    }

    public void setIsdoNum(int isdoNum) {
        this.isdoNum = isdoNum;
    }

    public int getTotalNum() {
        return totalNum;
    }

    public void setTotalNum(int totalNum) {
        this.totalNum = totalNum;
    }
}
